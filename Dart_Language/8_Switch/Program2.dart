//in dart-switch case other datatype are allowed it not give error but also it not give exact answer it goes in default block

void main(){

        String x = "Monday";
        switch(x){
                case 1:
                        print("one");
                case 2:
                        print("two");
                case 3:
                        print("three");
                case 4:
                        print("four");
                default:
                        print("No match");
        }
}

